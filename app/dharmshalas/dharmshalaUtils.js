// Utility functions for filtering and searching
export const searchDharmshalas = (query) => {
  if (!query) return dharmshalas;
  
  const lowercaseQuery = query.toLowerCase();
  return dharmshalas.filter(dharmshala => 
    dharmshala.name.toLowerCase().includes(lowercaseQuery) ||
    dharmshala.location.toLowerCase().includes(lowercaseQuery) ||
    dharmshala.address.toLowerCase().includes(lowercaseQuery) ||
    dharmshala.managers.some(manager => 
      manager.name.toLowerCase().includes(lowercaseQuery)
    )
  );
};

export const filterByState = (state) => {
  if (!state) return dharmshalas;
  
  return dharmshalas.filter(dharmshala => 
    dharmshala.location.includes(state)
  );
};

export const getUniqueStates = () => {
  const states = dharmshalas.map(dharmshala => {
    const parts = dharmshala.location.split(', ');
    return parts[parts.length - 1];
  });
  return [...new Set(states)].sort();
};